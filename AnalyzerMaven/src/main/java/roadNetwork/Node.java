package roadNetwork;

public class Node {
	
	private double x;
	private double y;
	
	public Node(double x, double y){
		this.x = x;
		this.y = y;
	}
	
	public double getX(){
		return this.x;
	}
	
	public double getY(){
		return this.y;
	}

}
